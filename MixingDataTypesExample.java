public class MixingDataTypesExample {
    public static void main(String[] args) {
        int intNum = 10;
        double doubleNum = 5.75;
        double sum = intNum + doubleNum; 
        System.out.println("Integer Number: " + intNum);
        System.out.println("Double Number: " + doubleNum);
        System.out.println("Sum of int and double: " + sum);
    }
}
