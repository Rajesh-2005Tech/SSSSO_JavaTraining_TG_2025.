public class Boolean {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("a AND b (a && b): " + (a && b));  
        System.out.println("a OR b  (a || b): " + (a || b));  
        System.out.println("NOT a   (!a): " + (!a));          
        System.out.println("NOT b   (!b): " + (!b));         
        System.out.println("a XOR b (a ^ b): " + (a ^ b));    
        System.out.println("a == b (Equality): " + (a == b)); 
        System.out.println("a != b (Not Equal): " + (a != b));
    }
}
